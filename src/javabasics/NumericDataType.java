package javabasics;

public class NumericDataType {
public static void main(String[] args) {
	byte c = 127;
	
	byte negvalue =-128;
	
	System.out.println(c);
	System.out.println(negvalue);
	
	short s = 10;
	
	short ss = 32767;
	
	short s1 = -32768;
	System.out.println(s1);
	System.out.println(ss);
	System.out.println(s);
	
	
	
	int maxsize = 2147483647;
	
	int minvalue = -2147483648;
	System.out.println(maxsize);
	
	System.out.println(minvalue);
	
	int i1=10;
	int i2 =20;
	int i3 = i1+i2;
	int i4 = i1-i2;
	int i5 = i1*i2;
	int i6 = i1/i2;
	System.out.println(i3);  //addition
	System.out.println(i4); //subtraction
	System.out.println(i5);//multiplication
	System.out.println(i6);//dividation
	  
	
	//long
	long l = 5454545454545451l;
	System.out.println(l);
	
	
	//    floting point data type 
	
	
	float f = 56.23f;
	
	float f1 = 3.45656556565656f;
	
	double d = 1.56565656256554655656;
	System.out.println(f);
	System.out.println(f1);
	System.out.println(d);
	
	
	

	
	
}
}
